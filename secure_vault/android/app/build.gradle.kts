                "META-INF/NOTICE.md",
                "META-INF/*.kotlin_module",
                "META-INF/versions/**",
                "META-INF/INDEX.LIST"
            )
            pickFirsts += setOf(
                "META-INF/AL2.0",
                "META-INF/LGPL2.1",
                "**/*.kotlin_module"
            )
            merges += setOf(
                "META-INF/services/*"
